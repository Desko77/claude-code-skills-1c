"""text_stage.py - общая ТЕКСТОВАЯ стадия для обоих движков (локаль и облако).

Работает поверх ГОТОВОГО транскрипта + описаний экрана - видео/аудио уже не трогает.
LLM передаётся как callable `llm(data_text, instruction) -> str`, поэтому модуль одинаково
работает и с gemma на сервере 150 (локальный движок), и с Gemini (облачный).

Стадии:
  1. map_speakers       - LLM определяет реальные имена спикеров из транскрипта -> {метка: имя}.
  2. apply_names        - подставить имена вместо анонимных меток в любом тексте.
  3. build_coherent_log - LLM собирает СВЯЗНЫЙ нарратив из механической склейки (экран+речь),
                          чанкуя длинный лог под контекст модели.
  4. build_summary      - 2-проходное саммари (экстракт всех фактов -> протокол), с дроблением.
"""
import json
import re

# --- Промпты (единый источник истины для обоих движков) ---

PROMPT_SPEAKERS = """Ниже транскрипт рабочей встречи: каждая реплика помечена анонимной меткой спикера (SPEAKER_00, SPEAKER_01, ... или "Участник N") и таймкодом. Определи реальное имя за каждой меткой, рассуждая как человек, который слушает встречу и по ходу понимает, кто есть кто:

- ОБРАЩЕНИЕ: если звучит «Имя, ...», «Имя, что скажешь?» - названный человек это тот, кто отвечает в СОСЕДНИХ (следующих по таймкоду) репликах, или к кому явно обращаются.
- САМОПРЕДСТАВЛЕНИЕ / подпись: «я, Имя, сделаю...», «это Имя», «меня зовут...».
- ССЫЛКИ: «как Имя говорил(а)...», «Имя предлагал(а)...» - привяжи имя к той метке, которая это ранее говорила.
- Согласуй по всей встрече: если метка один раз уверенно названа - держи это имя за ней везде.

Правила:
- Если человека в записи НИ РАЗУ не назвали - метку НЕ включай в ответ (не угадывай).
- Имя бери как звучит (обычно имя; можно имя-фамилию). Приводи к именительному падежу (звательную/усечённую форму «Саш», «Кать» разворачивай в полную).
- Одна метка = одно имя; одно имя не вешай на две разные метки.

Ответь СТРОГО одним JSON-объектом вида {"SPEAKER_00": "<имя>", "SPEAKER_01": "<имя>"}. Без markdown, без пояснений, без тройных кавычек."""

PROMPT_COHERENT = """Ниже фрагмент механического лога встречи: по таймкодам идут описания того, что распознано на экране (кадры), и реплики речи за те же интервалы. Перепиши это в СВЯЗНЫЙ хронологический нарратив - единым текстом рассказывай, что показывали на экране и что при этом обсуждали участники.

Требования:
- Каждый смысловой абзац начинай с таймкода [MM:SS].
- Указывай говорящего по имени, если оно есть в репликах.
- СТРОГО только из данного материала, НИЧЕГО не выдумывай.
- Цифры, суммы, названия (справочников, документов, счетов, файлов) - сохраняй ДОСЛОВНО.
- Пиши по-русски, связно и по делу.

Верни только сам нарратив, без вводных фраз."""

PROMPT_TASKS_EXTRACT = """Перед тобой полная дословная транскрипция рабочей встречи. Извлеки из нее АБСОЛЮТНО ВСЕ конкретные пункты, ничего не обобщая и не пропуская.

Пройди транскрипцию последовательно от начала до конца и выпиши:

## Задачи и поручения
Каждую задачу, поручение, договоренность что-то сделать - отдельным пунктом. Даже если упомянуто вскользь, одной фразой, между делом. Для каждой: что сделать, кто ответственный (если назван), срок (если назван), таймкод [MM:SS].

## Решения
Каждое принятое решение - отдельным пунктом, с таймкодом. ВАЖНО: если решение составное (договорились сразу о нескольких связанных вещах - например и о механизме, и о его параметрах, и о способе ввода), РАЗБЕЙ его на отдельные пункты, а не склеивай в один.

## Открытые вопросы
Каждый незакрытый вопрос, разногласие, "надо уточнить" - отдельным пунктом, с таймкодом.

## Важные детали
Прочие значимые факты: цифры, условия, названия систем и документов, сроки, которые прозвучали и могут понадобиться.

Правила:
- Полнота и дробность важнее краткости. Лучше включить лишнее и раздробить, чем упустить или склеить.
- НЕ объединяй несколько пунктов в один. Каждое поручение и каждое решение - отдельной строкой.
- Сохраняй конкретику дословно: имена, цифры, названия, сроки.
- Только то, что реально прозвучало в транскрипции. Ничего не выдумывай.

Отвечай на языке транскрипции."""

PROMPT_SUMMARY_FROM_TEXT = """Перед тобой полная транскрипция рабочей встречи и предварительно извлеченный из нее список фактов (задачи, решения, открытые вопросы, детали). Составь по ним структурированный протокол встречи.

КЛЮЧЕВОЕ ТРЕБОВАНИЕ: протокол обязан включить ВСЕ пункты из списка фактов - ни одна задача, решение или открытый вопрос не должны потеряться. Список фактов - это контроль полноты; транскрипция - источник контекста, формулировок и связей.

Формат протокола (строго соблюдай структуру и заголовки):

---

## Цель встречи
Один абзац - зачем собрались, что хотели обсудить/решить.

## Участники
Список участников с именами и ролями (если определяются).

## Ключевые темы и фокус обсуждения
Нумерованный список основных тем. Каждая тема - заголовок и 1-2 предложения пояснения.

## Решения
Нумерованный список всех принятых решений. Каждое - отдельным пунктом, подробно, с таймкодом.

## Открытые вопросы
Нумерованный список всех нерешенных вопросов. Для каждого - почему отложено или что нужно для решения, с таймкодом.

## Задачи
Группируй по ответственному. Для каждого человека - нумерованный список задач.
Формат:
### Имя (Роль)
1. Описание задачи. Срок: дата или "не определен". Таймкод [MM:SS]
2. ...
Задачи без явного ответственного - в группу "### Без ответственного".

---

Отвечай на языке транскрипции. По делу, но с достаточной детализацией, чтобы человек не присутствовавший на встрече понял контекст. Перепроверь себя: каждый факт из списка должен найти место в протоколе."""


# --- Стадии ---

def map_speakers(transcript_text, llm, max_chars=48000):
    """LLM определяет имена спикеров по репликам. Возвращает {метка: имя} (пустой при неудаче).

    max_chars большой, чтобы охватить весь транскрипт - имена спикеров часто звучат ближе
    к концу (кого-то называют позже). Текстовый маппинг слабее облачного (тот мэпит по голосу):
    кого по репликам определить нельзя - остаётся меткой.
    """
    if not transcript_text.strip():
        return {}
    resp = llm(transcript_text[:max_chars], PROMPT_SPEAKERS)
    try:
        m = re.search(r"\{.*\}", resp, re.S)
        data = json.loads(m.group(0)) if m else {}
        return {str(k): str(v) for k, v in data.items() if v and str(v) != str(k)}
    except (json.JSONDecodeError, ValueError, AttributeError, TypeError):
        return {}


def apply_names(text, name_map):
    """Заменить метки спикеров на имена ОДНИМ проходом (комбинированный regex, подстановка по словарю).

    Один проход, а не цепочка re.sub: уже подставленное имя НЕ может быть повторно перезаписано
    следующей меткой. Это устраняет каскадное схлопывание, если LLM перепутал метки местами (напр.
    вернул SPEAKER_00->SPEAKER_01): при последовательных re.sub речь двух спикеров склеилась бы в одно
    имя. Метки в альтернации длинными первыми (SPEAKER_00 матчится раньше SPEAKER_0)."""
    if not name_map or not text:
        return text
    labels = sorted(name_map, key=len, reverse=True)
    pattern = re.compile("|".join(re.escape(lbl) for lbl in labels))
    return pattern.sub(lambda m: name_map[m.group(0)], text)


def _split_frame_blocks(mechanical_log):
    """Разбить механический лог на блоки-кадры (граница - строка, начинающаяся с '## [' или '[MM:SS]')."""
    blocks = re.split(r"(?=^#{0,3}\s*\[\d{1,2}:\d{2}\])", mechanical_log, flags=re.M)
    return [b for b in blocks if b.strip()]


def build_coherent_log(mechanical_log, llm, chunk_chars=18000):
    """Связный нарратив из механической склейки (экран+речь). Чанкуем под контекст модели.

    Каждый чанк - несколько кадров подряд; на выходе нарративы конкатенируются по порядку.
    """
    blocks = _split_frame_blocks(mechanical_log)
    if not blocks:
        return ""
    chunks, cur, cur_len = [], [], 0
    for b in blocks:
        if cur and cur_len + len(b) > chunk_chars:
            chunks.append("".join(cur))
            cur, cur_len = [], 0
        cur.append(b)
        cur_len += len(b)
    if cur:
        chunks.append("".join(cur))

    out = []
    for i, ch in enumerate(chunks, 1):
        narrative = llm(ch, PROMPT_COHERENT).strip()
        if narrative:
            out.append(narrative)
    return "\n\n".join(out)


def build_summary(transcript_text, llm):
    """2-проходное саммари: экстракт всех фактов -> протокол с контролем полноты."""
    if not transcript_text.strip():
        return None
    facts = llm(transcript_text, PROMPT_TASKS_EXTRACT)
    combined = (f"ТРАНСКРИПЦИЯ:\n\n{transcript_text}\n\n"
                f"---\n\nПРЕДВАРИТЕЛЬНО ИЗВЛЕЧЕННЫЕ ФАКТЫ:\n\n{facts}")
    return llm(combined, PROMPT_SUMMARY_FROM_TEXT)
